/// Returns the 8-byte glyph for [codePoint], or `null` when it is outside the
/// printable ASCII range [firstPrintableAscii]–[lastPrintableAscii].
List<int>? glyphForCodePoint(int codePoint) {
  if (codePoint < firstPrintableAscii || codePoint > lastPrintableAscii) {
    return null;
  }
  return font8x8Basic[codePoint - firstPrintableAscii];
}

/// Whether the glyph for [codePoint] has ink at pixel ([x], [y]).
///
/// [x] is the column (0 = leftmost), [y] is the row (0 = top). Out-of-range
/// coordinates or unsupported code points return `false`. Mirrors the LSB-first
/// row encoding documented on the library: bit `1 << x` of row byte `y`.
bool glyphPixel(int codePoint, int x, int y) {
  if (x < 0 || x >= glyphWidth || y < 0 || y >= glyphHeight) {
    return false;
  }
  final glyph = glyphForCodePoint(codePoint);
  if (glyph == null) {
    return false;
  }
  return (glyph[y] & (1 << x)) != 0;
}
