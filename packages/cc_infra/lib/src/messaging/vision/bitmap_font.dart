/// Embedded 8x8 monochrome ASCII bitmap font for the vision compaction
/// renderer.
///
/// This is the classic public-domain `font8x8_basic` table (CC0, by Daniel
/// Hepper, derived from the IBM PC BIOS 8x8 glyphs) covering printable ASCII
/// `0x20`–`0x7E`. The pixel font is legible at native size and lets the
/// monochrome renderer stamp any normalized character with a real glyph rather
/// than a procedurally generated shape.
///
/// ## Bit layout
///
/// Each glyph is exactly 8 bytes — one byte per row, top row first. Within a
/// row byte, **bit 0 (the least-significant bit, value `0x01`) is the LEFTMOST
/// pixel** and bit 7 (`0x80`) is the rightmost. A set bit is ink (a drawn
/// pixel); a clear bit is background. This matches the upstream
/// `font8x8_basic` orientation, so the glyphs read correctly when blitted
/// left-to-right with [glyphPixel].
